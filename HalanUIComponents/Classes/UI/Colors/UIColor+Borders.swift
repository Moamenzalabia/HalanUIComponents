import UIKit

extension UIColor {
    /// Universal color (Hex) #007AFF
    public static var BlueBorder: UIColor {
        UIColor(named: "BlueBorder") ?? black
    }
    
    /// Universal color (Hex) #EB5757
    public static var DangerBorder: UIColor {
        UIColor(named: "DangerBorder") ?? black
    }
}
